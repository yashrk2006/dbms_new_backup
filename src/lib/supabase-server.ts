import { createServerClient, type CookieOptions } from '@supabase/ssr';
import { createClient } from '@supabase/supabase-js';
import { cookies } from 'next/headers';

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL!;
const supabaseAnonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY!;

/**
 * Server client for Server Components, Actions, and Middleware.
 * handles cookie persistence for authentication sessions.
 */
export async function createClientServer() {
  const cookieStore = await cookies();

  return createServerClient(supabaseUrl, supabaseAnonKey, {
    cookies: {
      get(name: string) {
        return cookieStore.get(name)?.value;
      },
      set(name: string, value: string, options: CookieOptions) {
        try {
          cookieStore.set({ name, value, ...options });
        } catch (error) {
          // Can be ignored if middleware refreshes sessions
        }
      },
      remove(name: string, options: CookieOptions) {
        try {
          cookieStore.set({ name, value: '', ...options });
        } catch (error) {
          // Can be ignored if middleware refreshes sessions
        }
      },
    },
  });
}

/**
 * Admin client for high-privilege operations (RLS bypass).
 * Use for verifying Roll Numbers in the institutional directory.
 */
export const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey);
